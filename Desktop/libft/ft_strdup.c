#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char		*s2;
	size_t		len;
	int			i;

	len = ft_strlen(s1);
	s2 = (char *)malloc(len + 1);
	if (!(s2))
		return (0);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = 0;
	return (s2);
}
